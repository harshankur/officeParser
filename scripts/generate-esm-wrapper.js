/**
 * ESM Wrapper Generator
 *
 * Generates dist/index.mjs — a thin ESM re-export from the CJS dist/index.js.
 *
 * This enables dual CJS/ESM support without the "dual package hazard":
 * - Only one copy of actual code exists (CJS from tsc)
 * - ESM consumers (`import`) get proper named exports via this wrapper
 * - CJS consumers (`require`) use dist/index.js directly
 *
 * The named exports list is derived from dist/index.js at build time,
 * ensuring the wrapper stays in sync with the actual exports.
 */

const fs = require('fs');
const path = require('path');

// Read the compiled CJS output to extract exported names
const projectRoot = path.join(__dirname, '..');
const cjsPath = path.join(projectRoot, 'dist', 'index.js');
if (!fs.existsSync(cjsPath)) {
    console.error('Error: dist/index.js not found. Run `npm run build:node` first.');
    process.exit(1);
}

let mod;
try {
    mod = require(cjsPath);
} catch (err) {
    console.error('Error requiring dist/index.js:', err);
    process.exit(1);
}

// Get all exported keys, omitting metadata and default exports
const names = Object.keys(mod).filter(
    k => k !== '__esModule' && k !== 'default'
);

if (names.length === 0) {
    console.error('Error: No named exports found in dist/index.js.');
    process.exit(1);
}

// Build the ESM wrapper content
const wrapperContent = `/**
 * ESM wrapper for officeparser
 *
 * AUTO-GENERATED — do not edit manually.
 * Generated by scripts/generate-esm-wrapper.js during build.
 *
 * This file re-exports from the CJS build (dist/index.js) to provide
 * proper ESM named exports without duplicating the source code.
 */

import _module from './index.js';

// Named exports
const { ${names.join(', ')} } = _module;
export { ${names.join(', ')} };

// Default export
export default _module.default ?? _module;
`;

const outPath = path.join(projectRoot, 'dist', 'index.mjs');
fs.writeFileSync(outPath, wrapperContent, 'utf8');

console.log(`Generated dist/index.mjs with exports: ${names.join(', ')}`);
