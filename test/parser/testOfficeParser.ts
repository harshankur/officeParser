/** Run feature tests for a single file */
async function testFile(ext: string): Promise<FeatureTest[]> {
    const filePath = getFilePath(ext);
    if (!fs.existsSync(filePath)) {
        return [{
            category: 'File',
            feature: 'Exists',
            fileType: ext,
            result: {
                status: 'SKIP',
                expected: true,
                actual: false,
                details: 'Test file not found',
                duration: 0
            }
        }];
    }

    const startTime = Date.now();

    try {
        const ast = await OfficeParser.parseOffice(filePath, FULL_CONFIG);
        fs.writeFileSync(`${getOutputPath(ext)}.actual.json`, JSON.stringify(ast, null, 2), 'utf-8');
        fs.writeFileSync(`${getOutputPath(ext)}.actual.txt`, (await ast.to('text')).value, 'utf-8');
        const metrics = extractMetrics(ast);
        const results: FeatureTest[] = [];

        // Add text extraction validation
        results.push(...validateTextExtraction('Text Extraction', ext, (await ast.to('text')).value, metrics));
        if (ext === 'xlsx') {
            results.push(...await testXlsxInlineStrings());
        }

        // Load baseline if available
        const baseline = loadBaseline(ext);
        if (baseline) {
            // Add metrics comparison
            const baselineMetrics = extractMetrics(baseline);
            results.push(...compareMetrics('Full Extraction', ext, baselineMetrics, metrics));

            // Custom properties: verify actual key-value pairs against baseline.
            // Kept separate from compareMetrics so parity tests (which compare across formats
            // that may not support custom properties) are not affected.
            if (baselineMetrics.metadata.hasCustomProperties || metrics.metadata.hasCustomProperties) {
                const expectedProps = baseline.metadata?.customProperties ?? {};
                const actualProps = ast.metadata?.customProperties ?? {};
                const allKeys = new Set([...Object.keys(expectedProps), ...Object.keys(actualProps)]);
                for (const key of allKeys) {
                    const expected = expectedProps[key];
                    const actual = actualProps[key];
                    // Coerce both to string for comparison to handle Date serialization
                    const match = String(expected) === String(actual);
                    results.push({
                        category: 'Full Extraction',
                        feature: `Custom Property: ${key}`,
                        fileType: ext,
                        result: {
                            status: match ? 'PASS' : 'FAIL',
                            expected,
                            actual,
                            details: match ? `${key} = ${actual}` : `Expected ${key} = ${expected}, got ${actual}`
                        }
                    });
                }
            }

            // Add text baseline comparison
            const extractedText = (await ast.to('text')).value;
            results.push(...compareTextBaseline('Text Baseline', ext, extractedText));
        } else {
            // No baseline - just report metrics
            results.push({
                category: 'Metrics',
                feature: 'Extraction',
                fileType: ext,
                result: {
                    status: 'WARN',
                    expected: 'Baseline pending',
                    actual: JSON.stringify(metrics),
                    details: `Extracted ${metrics.contentNodes} nodes (no baseline to compare)`
                }
            });
        }

        const totalDuration = Date.now() - startTime;
        results.forEach(r => {
            if (r.result.duration === undefined) r.result.duration = totalDuration;
        });

        return results;
    } catch (error: any) {
        return [{
            category: 'Parsing',
            feature: 'Parse',
            fileType: ext,
            result: {
                status: 'FAIL',
                expected: 'Success',
                actual: 'Error',
                details: error.message,
                duration: Date.now() - startTime
            }
        }];
    }
}

/** Run cross-format parity tests */
async function testGroupParity(group: string[], groupName: string): Promise<FeatureTest[]> {
    const results: FeatureTest[] = [];

    // Find the baseline format in the group
    const baselineFormat = group.find(ext => BASELINE_STATUS[ext as keyof typeof BASELINE_STATUS]);
    if (!baselineFormat) {
        return [{
            category: 'Group Parity',
            feature: groupName,
            fileType: 'N/A',
            result: {
                status: 'SKIP',
                expected: 'Baseline',
                actual: 'None',
                details: 'No baseline available in group',
                duration: 0
            }
        }];
    }

    const startTotalTime = Date.now();

    // Parse baseline
    const baselinePath = getFilePath(baselineFormat);
    const baselineAST = await OfficeParser.parseOffice(baselinePath, FULL_CONFIG);
    const baselineMetrics = extractMetrics(baselineAST);

    // Compare each file in group to baseline
    for (const ext of group) {
        if (ext === baselineFormat) continue;

        const startTime = Date.now();
        const filePath = getFilePath(ext);
        if (!fs.existsSync(filePath)) {
            results.push({
                category: `${groupName} Parity`,
                feature: `${ext} vs ${baselineFormat}`,
                fileType: ext,
                result: {
                    status: 'SKIP',
                    expected: 'File exists',
                    actual: 'Missing',
                    details: 'Test file not found',
                    duration: 0
                }
            });
            continue;
        }

        try {
            const actualAST = await OfficeParser.parseOffice(filePath, FULL_CONFIG);
            const actualMetrics = extractMetrics(actualAST);

            // Apply file-type specific adjustments
            let adjustedMetrics = actualMetrics;
            let adjustmentNotes: Record<string, string> = {};

            // ODP: Apply logical adjustments for structural differences
            if (ext === 'odp') {
                const odpResult = applyOdpParityAdjustments(actualMetrics, actualAST, baselineMetrics);
                adjustedMetrics = odpResult.adjusted;
                adjustmentNotes = odpResult.notes;
            }

            // ODT/RTF: Check if link count difference is due to tab-character link nodes (TOC entries)
            if ((ext === 'odt' || ext === 'rtf') && actualMetrics.links.total > baselineMetrics.links.total) {
                const tabLinkCount = countTabLinkNodes(actualAST);
                const baselineTabLinks = countTabLinkNodes(baselineAST);
                const tabLinkDiff = tabLinkCount - baselineTabLinks;

                // If the difference in total links equals the difference in tab links,
                // adjust metrics to match baseline for comparison
                if (tabLinkDiff > 0 && (actualMetrics.links.total - baselineMetrics.links.total) === tabLinkDiff) {
                    adjustedMetrics = {
                        ...adjustedMetrics,
                        links: {
                            ...adjustedMetrics.links,
                            total: adjustedMetrics.links.total - tabLinkDiff
                        }
                    };
                    adjustmentNotes['Links - Total'] = `Adjusted for ${tabLinkDiff} TOC tab-character links`;
                }
            }

            // PDF Limitations: Use dedicated comparison function with format-aware logic
            if (ext === 'pdf') {
                const pdfParity = comparePdfParity(groupName, baselineMetrics, adjustedMetrics);
                results.push(...pdfParity);
                continue;
            }

            // RTF Limitations: Use dedicated comparison function with format-aware logic
            if (ext === 'rtf') {
                const rtfParity = compareRtfParity(`${groupName} Parity`, baselineMetrics, adjustedMetrics);
                results.push(...rtfParity);
                continue;
            }

            // HTML Parity
            if (ext === 'html') {
                const htmlParity = compareHtmlParity(baselineMetrics, adjustedMetrics);
                results.push(...htmlParity);
                continue;
            }

            // MD Parity
            if (ext === 'md') {
                const mdParity = compareMdParity(baselineMetrics, adjustedMetrics);
                results.push(...mdParity);
                continue;
            }

            // EPUB Parity
            if (ext === 'epub') {
                const epubParity = compareEpubParity(baselineMetrics, adjustedMetrics);
                results.push(...epubParity);
                continue;
            }

            // CSV Parity
            if (ext === 'csv') {
                const csvParity = compareCsvParity(baselineMetrics, adjustedMetrics);
                results.push(...csvParity);
                continue;
            }

            // Compare - accounting for format limitations (for non-PDF/RTF)
            results.push(...compareMetrics(`${groupName} Parity`, ext, baselineMetrics, adjustedMetrics, true, adjustmentNotes));
        } catch (error: any) {
            results.push({
                category: `${groupName} Parity`,
                feature: `${ext} vs ${baselineFormat}`,
                fileType: ext,
                result: {
                    status: 'FAIL',
                    expected: 'Parse success',
                    actual: 'Error',
                    details: error.message
                }
            });
        }

        // Assign duration to group parity results for this file
        const fileDuration = Date.now() - startTime;
        results.filter(r => r.fileType === ext && r.category === `${groupName} Parity`).forEach(r => {
            if (r.result.duration === undefined) r.result.duration = fileDuration;
        });
    }

    return results;
}

/** Run config permutation tests */
async function testConfigs(ext: string): Promise<FeatureTest[]> {
    const filePath = getFilePath(ext);
    if (!fs.existsSync(filePath)) {
        return [];
    }

    const results: FeatureTest[] = [];

    for (const configTest of CONFIG_TESTS) {
        const startTime = Date.now();
        try {
            const ast = await OfficeParser.parseOffice(filePath, { ...FULL_CONFIG, ...configTest.config });

            // Validate config effects
            const hasAttachments = ast.attachments && ast.attachments.length > 0;
            const hasOCR = ast.attachments && ast.attachments.some(a => a.ocrText != undefined);
            const hasRaw = ast.content.some(n => (n as any).rawContent);

            // Note validation
            const checkHasNotes = (nodes: any[]): boolean => {
                return nodes.some(node =>
                    node.type === 'note' ||
                    (node.notes && node.notes.length > 0) ||
                    (node.children && checkHasNotes(node.children))
                );
            };
            const hasNotes = checkHasNotes(ast.content);

            const checkHasComments = (nodes: any[]): boolean => {
                return nodes.some(node =>
                    (node.comments && node.comments.length > 0) ||
                    (node.children && checkHasComments(node.children))
                );
            };
            const hasComments = checkHasComments(ast.content);

            const hasHeaders = ast.auxiliary?.headers && ast.auxiliary.headers.length > 0;
            const hasFooters = ast.auxiliary?.footers && ast.auxiliary.footers.length > 0;
            const hasHeadersOrFooters = Boolean(hasHeaders || hasFooters);
            const hasSlideMasters = Boolean(ast.auxiliary?.slideMasters && ast.auxiliary.slideMasters.length > 0);

            const checkHasInternalLinks = (nodes: any[]): boolean => {
                return nodes.some(node =>
                    (node.type === 'link' && node.metadata?.internal) ||
                    (node.children && checkHasInternalLinks(node.children))
                );
            };
            const hasInternalLinks = checkHasInternalLinks(ast.content);

            let status: 'PASS' | 'FAIL' | 'WARN' = 'PASS';
            let details = 'Config behavior correct';

            // Check specific configs
            if (configTest.id === 'C6') { // Ignore Notes
                if (hasNotes) {
                    status = 'FAIL';
                    details = 'Ignore Notes: Found notes but expected none';
                }
            } else if (configTest.id === 'C7') { // Ignore Comments
                if (hasComments) {
                    status = 'FAIL';
                    details = 'Ignore Comments: Found comments but expected none';
                }
            } else if (configTest.id === 'C10') { // Ignore Headers and Footers
                if (hasHeadersOrFooters) {
                    status = 'FAIL';
                    details = 'Ignore Headers/Footers: Found auxiliary content but expected none';
                }
            } else if (configTest.id === 'C11') { // Ignore Slide Masters
                if (hasSlideMasters) {
                    status = 'FAIL';
                    details = 'Ignore Slide Masters: Found slide masters but expected none';
                }
            } else if (configTest.id === 'C12') { // Ignore Internal Links
                if (hasInternalLinks) {
                    status = 'FAIL';
                    details = 'Ignore Internal Links: Found internal links but expected none';
                }
            }

            // General attachment check (skip if checking notes specifically to avoid noise, or keep it?)
            // Keeping existing check for other configs
            const configExpected = configTest.config.extractAttachments;
            const configActual = hasAttachments;

            if (configTest.id !== 'C6' && configTest.id !== 'C7') {
                if (configExpected !== undefined && configExpected !== configActual) {
                    // Skip for formats that don't support attachments (inherent limitation)
                    if (configExpected === true && !configActual && ext === 'csv') {
                        status = 'PASS';
                        details = 'CSV format does not support attachments (expected)';
                    } else {
                        status = 'FAIL';
                        details = `Attachments mismatch: Expected ${configExpected}, got ${configActual}`;
                    }
                }
            }

            results.push({
                category: 'Config Test',
                feature: `${configTest.id}: ${configTest.name}`,
                fileType: ext,
                result: {
                    status: status,
                    expected: configTest.config,
                    actual: { hasAttachments, hasOCR, hasRaw, hasNotes, hasComments, hasHeadersOrFooters, hasSlideMasters, hasInternalLinks },
                    details: details
                }
            });
        } catch (error: any) {
            results.push({
                category: 'Config Test',
                feature: `${configTest.id}: ${configTest.name}`,
                fileType: ext,
                result: {
                    status: 'FAIL',
                    expected: 'Parse success',
                    actual: 'Error',
                    details: error.message,
                    duration: Date.now() - startTime
                }
            });
        }

        // Assign duration to config results for this permutation
        const configDuration = Date.now() - startTime;
        results.filter(r => r.fileType === ext && r.category === 'Config Test' && r.feature.startsWith(configTest.id)).forEach(r => {
            if (r.result.duration === undefined) r.result.duration = configDuration;
        });
    }
    return results;
}

