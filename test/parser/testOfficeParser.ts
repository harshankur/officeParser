/**
 * EPUB-specific parity comparison against the DOCX baseline.
 *
 * test.epub was itself generated by converting test.docx (via calibre), so the two
 * should be structurally close - but calibre's DOCX-to-EPUB conversion introduces its
 * own well-understood differences, verified empirically against this exact fixture pair:
 * - Tables and heading counts: calibre preserves these exactly - compared strictly.
 * - Content nodes / lists: calibre restructures paragraph/list nesting somewhat
 *   (e.g. flattening multi-level lists into flat sibling lists that use CSS classes for
 *   indent - so all EPUB lists parse at indentation level 0), so counts are close but
 *   not identical. NOTE: this is calibre's flattening, not a parser limitation -
 *   officeParser's own DOCX->EPUB preserves nesting levels exactly.
 * - Images: calibre's conversion of THIS document happened to drop the main content
 *   image entirely (keeping only a tiny decorative gif, unreferenced). The fixture was
 *   patched to re-insert that image (image.jpg from the DOCX) inline at its original
 *   position under the "Images" heading, so the in-content image count now matches DOCX
 *   exactly and is compared strictly.
 * - Links: DOCX's internal cross-references (TOC, footnote backlinks) become
 *   cross-file anchor links once calibre splits the document into multiple XHTML
 *   files, which HtmlParser classifies as 'external' (no leading '#') - so the
 *   internal/external split differs even when the total is close.
 * - Formatting: calibre expresses DOCX run-level formatting (bold/color/size/etc.) via
 *   CSS classes in an external stylesheet, not inline style attributes or semantic
 *   tags, so HtmlParser's formatting detection under-counts relative to DOCX's
 *   styleMap-driven formatting - compared as informational only.
 * - Metadata: calibre keeps Dublin Core fields (title/author) but drops DOCX's custom
 *   document properties - only presence of title/author is asserted, not equality.
 */
function compareEpubParity(expected: FeatureMetrics, actual: FeatureMetrics): FeatureTest[] {
    const results: FeatureTest[] = [];
    const category = 'Documents Parity';
    const fileType = 'epub';

    const createResult = (feature: string, exp: any, act: any, condition: boolean, details: string, status?: 'PASS' | 'FAIL' | 'WARN'): FeatureTest => ({
        category, feature, fileType,
        result: { status: status ?? (condition ? 'PASS' : 'FAIL'), expected: exp, actual: act, details }
    });

    // Tables: calibre's DOCX->EPUB conversion preserves table structure exactly.
    const tableMatch = actual.tables.total === expected.tables.total && actual.tables.rows === expected.tables.rows && actual.tables.cells === expected.tables.cells;
    results.push(createResult('Tables - Total', expected.tables.total, actual.tables.total, tableMatch, tableMatch ? `Tables match exactly: ${actual.tables.total} tables, ${actual.tables.rows} rows, ${actual.tables.cells} cells` : `Expected ${expected.tables.total} tables/${expected.tables.rows} rows/${expected.tables.cells} cells, got ${actual.tables.total}/${actual.tables.rows}/${actual.tables.cells}`));

    // Headings: total count is preserved exactly (byLevel distribution may shift by one
    // level here and there, which isn't tracked by the shared metrics comparison anyway).
    const headingMatch = actual.headings.total === expected.headings.total;
    results.push(createResult('Headings', expected.headings.total, actual.headings.total, headingMatch, `Expected ${expected.headings.total}, got ${actual.headings.total}`));

    // Content nodes / Lists: close but not identical - calibre's conversion restructures
    // some paragraph/list nesting.
    const nodeDiff = Math.abs(actual.contentNodes - expected.contentNodes) / Math.max(expected.contentNodes, 1);
    const nodeMatch = nodeDiff <= 0.2;
    results.push(createResult('Content Nodes', expected.contentNodes, actual.contentNodes, nodeMatch, `${(nodeDiff * 100).toFixed(1)}% of original nodes`, nodeMatch ? 'PASS' : 'WARN'));

    const listMatch = actual.lists.total === expected.lists.total;
    results.push(createResult('Lists - Total', expected.lists.total, actual.lists.total, listMatch, `Expected ${expected.lists.total}, got ${actual.lists.total} (calibre may flatten multi-level lists)`, listMatch ? 'PASS' : 'WARN'));

    // Images: the fixture was patched to carry the same inline image as the DOCX (see the
    // header note), so the in-content image count is expected to match exactly.
    const imageMatch = actual.images === expected.images;
    results.push(createResult('Images', expected.images, actual.images, imageMatch, imageMatch ? `Inline image count matches DOCX: ${actual.images}` : `Expected ${expected.images}, got ${actual.images}`));

    // Links: cross-file anchors from calibre's document splitting count as 'external'
    // rather than 'internal', so only informational.
    const linkMatch = actual.links.total === expected.links.total;
    results.push(createResult('Links - Total', expected.links.total, actual.links.total, linkMatch, `EPUB: ${actual.links.total} links (DOCX: ${expected.links.total}) - cross-file anchors from document splitting count differently`, linkMatch ? 'PASS' : 'WARN'));

    // Formatting: CSS-class-based styling in the converted EPUB isn't detected by
    // HtmlParser's inline-style/tag-based formatting extraction - informational only.
    const formattingTotal = Object.values(actual.formatting).reduce((a: number, b) => a + (b as number), 0);
    results.push(createResult('Formatting - Present', 'Present', formattingTotal > 0 ? 'Present' : 'None', formattingTotal > 0, `EPUB formatting is largely CSS-class driven, not inline - ${formattingTotal} inline-detected marks (DOCX: ${Object.values(expected.formatting).reduce((a: number, b) => a + (b as number), 0)})`, formattingTotal > 0 ? 'PASS' : 'WARN'));

    // Metadata: Dublin Core title/author survive; custom properties don't.
    results.push(createResult('Metadata - Title', expected.metadata.hasTitle, actual.metadata.hasTitle, actual.metadata.hasTitle, `Dublin Core title extraction`));
    results.push(createResult('Metadata - Author', expected.metadata.hasAuthor, actual.metadata.hasAuthor, actual.metadata.hasAuthor, `Dublin Core author extraction`));

    return results;
}

