class DualLogger {
    private mdContent: string = '';

    log(message: string = '') {
        console.log(message);
        this.mdContent += message + '\n';
    }

    getMarkdown(): string {
        return '```\n' + this.mdContent + '```\n';
    }

    clear() {
        this.mdContent = '';
    }
}

